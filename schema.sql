-- =========================
-- BRANDS
-- =========================
CREATE TABLE brands (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    brand_description TEXT,

    CONSTRAINT uq_brands_name UNIQUE (name)
);

-- =========================
-- CATEGORIES
-- =========================
CREATE TABLE categories (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,

    CONSTRAINT uq_categories_name UNIQUE (name)
);

-- =========================
-- AREAS
-- =========================
CREATE TABLE areas (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,

    CONSTRAINT uq_areas_name UNIQUE (name)
);

-- =========================
-- PRODUCTS
-- =========================
CREATE TABLE products (
    id INT AUTO_INCREMENT PRIMARY KEY,

    name VARCHAR(255) NOT NULL,
    description TEXT,

    selling_price DECIMAL(10,2) NOT NULL,
    mrp DECIMAL(10,2) NOT NULL,

    material VARCHAR(100),
    color VARCHAR(50),

    rating DECIMAL(2,1) DEFAULT 0,
    stock INT DEFAULT 0,

    brand_id INT NOT NULL,

    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

    CONSTRAINT fk_products_brand 
        FOREIGN KEY (brand_id) 
        REFERENCES brands(id) 
        ON DELETE CASCADE,

    CONSTRAINT chk_products_price 
        CHECK (selling_price <= mrp),

    CONSTRAINT chk_products_rating 
        CHECK (rating >= 0 AND rating <= 5)
);

-- =========================
-- PRODUCT ↔ CATEGORIES
-- =========================
CREATE TABLE product_categories (
    product_id INT NOT NULL,
    category_id INT NOT NULL,

    PRIMARY KEY (product_id, category_id),

    CONSTRAINT fk_pc_product 
        FOREIGN KEY (product_id) 
        REFERENCES products(id) 
        ON DELETE CASCADE,

    CONSTRAINT fk_pc_category 
        FOREIGN KEY (category_id) 
        REFERENCES categories(id) 
        ON DELETE CASCADE
);

-- =========================
-- PRODUCT ↔ AREAS
-- =========================
CREATE TABLE product_areas (
    product_id INT NOT NULL,
    area_id INT NOT NULL,

    PRIMARY KEY (product_id, area_id),

    CONSTRAINT fk_pa_product 
        FOREIGN KEY (product_id) 
        REFERENCES products(id) 
        ON DELETE CASCADE,

    CONSTRAINT fk_pa_area 
        FOREIGN KEY (area_id) 
        REFERENCES areas(id) 
        ON DELETE CASCADE
);

-- =========================
-- PRODUCT IMAGES
-- =========================
CREATE TABLE product_images (
    id INT AUTO_INCREMENT PRIMARY KEY,
    product_id INT NOT NULL,

    image_url TEXT,
    is_primary BOOLEAN DEFAULT FALSE,
    sort_order INT,

    CONSTRAINT fk_pi_product 
        FOREIGN KEY (product_id) 
        REFERENCES products(id) 
        ON DELETE CASCADE,

    CONSTRAINT chk_pi_is_primary 
        CHECK (is_primary IN (0,1))
);


